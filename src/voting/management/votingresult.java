    public ArrayList<User1> userlist1()
    {
        ArrayList<User1> userlist1=new ArrayList<>();
        try{
            
            Connection  con = DriverManager.getConnection("jdbc:postgresql://localhost:5432/VOTING MANAGEMENT SYSTEM", 
                    "postgres", "dbms");
            String sql="select * from result5 where count=(select max(count) from result5)";
            Statement ps= con.createStatement();
             
            ResultSet rs=ps.executeQuery(sql);
            User1 user1;
            while(rs.next())
            {
            user1=new User1(rs.getInt("id"),rs.getString("partyname"),rs.getInt("count"));
           userlist1.add(user1);
            }
            
        }catch (SQLException ex) {
            Logger.getLogger(Addvoter.class.getName()).log(Level.SEVERE, null, ex);}
        return userlist1;
    }
    
    
    public void showuser()
    {
        ArrayList<User1> list= userlist1();
        DefaultTableModel model1=(DefaultTableModel) jvoteresult.getModel();
        
        Object[] row=new Object[10];
        for (int i = 0; i< list.size(); i++) {
            row[0]=list.get(i).getid();
            row[1]=list.get(i).getpatyname();
            row[2]=list.get(i).getcount();
            
            model1.addRow(row);
        }
    
    
    }
    
    
